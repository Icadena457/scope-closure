var firstName; //Declaracion
firstName = "Isabella"; //Asignacion
console.log(firstName); //Llamado

var lastName = "Cadena"; //Declarar y asignar
lastName = "Paola"; //Reasignacion
console.log(lastName);

var secondName = "Elcira"; // declarar / asignar
var secondName = "Pascal"; // reasignacion
console.log(secondName); // llamado

// let
let fruit = "Apple"; // declarar / asignar
fruit = "Kiwi"; // reasignar
console.log(fruit);

/* let fruit = "Banana"  => NO se puede redeclarar variables*/

